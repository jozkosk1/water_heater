	  SI7051_start_conversion(&hi2c1);
	    HAL_Delay(200);
	    temperature = SI7051_read_temperature(&hi2c1);

	    if(HAL_GetTick() - timestamp > 1000)
	    {
			  txCAN(&hcan, &header, txBuffer, TXMailbox,temperature); //send CAN message
			  timestamp = HAL_GetTick();
	    }
